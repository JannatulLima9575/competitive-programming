//Problem-3
#include <iostream>
using namespace std;

int main() {
    int X, Y;

    cin >> X >> Y;
    cout << X << " + " << Y << " = " << X + Y << endl;
    
    cin >> X >> Y;
    cout << X << " * " << Y << " = " << X * Y << endl;
        
    cin >> X >> Y;    
    cout << X << " - " << Y << " = " << X - Y << endl;

    return 0;
}

